	       .MODEL SMALL
	       .STACK 100H
	       .DATA
P1F		DW 0010H
P1C             DW 0050H     ;COORDENADAS DEL CUBO
P2F             DW 0070H
P2C             DW 0150H
P3F		DW 0030H
P3C		DW 0030H
P4F             DW 0090H
P4C             DW 0130H
COLUM		DW ?             ;CANTIDAD DE COLUMNAS
FILA		DW ?             ;CANTIDAD DE FILAS
F1		DW ?
C1		DW ?
COLU1           DW ?
FIL1            DW ?
F2		DW ?
C2		DW ?

		.code
		MOV AX,SEG P1F
		MOV DS,AX
		MOV AH,00H
		MOV AL,0FH
		INT 10H
		MOV AX,P2C
		SUB AX,P1C
		INC AX
		MOV COLUM,AX
		MOV AX,P2F
		SUB AX,P1F
		INC AX
		MOV FILA,AX
		MOV AX,P1F
		MOV F1,AX
		MOV AX,P1C
		MOV C1,AX
		CALL VERTICAL
		CALL HORIZONTAL
		MOV C1,CX        	;AQUI CARGO LA POSICION DEL CURSOR
		dec c1
		CALL VERTICAL           ;QUE ESTABA EN LA ESQUINA SUPERIOR
	       	MOV AX,P2F		;DERECHA POSTERIOR P2P
		MOV F1,AX
		MOV AX,P1C
		MOV C1,AX
		dec c1
		CALL HORIZONTAL
		MOV AX,P3F		
		MOV F1,AX
		MOV AX,P3C
		MOV C1,AX
		CALL VERTICAL
		CALL HORIZONTAL
		MOV C1,CX
		dec c1
		CALL HORIZONTAL
		MOV AX,P4F	
		MOV F1,AX
		MOV AX,P3C
		MOV C1,AX
		CALL HORIZONTAL
		MOV AX,P3F
		MOV F1,AX
		MOV AX,P3C
		MOV C1,AX
		MOV AX,P1F
		MOV F2,AX
		CALL DIAG
		MOV AX,P3F
		MOV F1,AX
		MOV AX,P4C
		MOV C1,AX
		CALL DIAG
		MOV AX,P4F
		MOV F1,AX
		MOV AX,P3C
		MOV C1,AX
		MOV AX,P2F
		MOV F2,AX
		CALL DIAG
		MOV AX,P4F
		MOV F1,AX
		MOV AX,P4C
		MOV C1,AX
		CALL DIAG
		MOV AH,00H
		INT 16H

 VERTICAL:	MOV AH,0CH		;DIBUJA PIXEL VERTICAL
		MOV AL,03H
		MOV BH,00H
		MOV CX,C1
		MOV DX,F1
		MOV DI,FILA
		MOV FIL1,DI
B1:		INT 10H
		INC DX
		DEC FIL1
		JNZ B1
		RETN
HORIZONTAL:	MOV AH,0CH		;DIBUJA PIXEL HORIZONTAL
		MOV AL,03H
		MOV BH,00H
		MOV CX,C1
		MOV DX,F1
		MOV DI,COLUM
		MOV COLU1,DI
H1:		INT 10H
		INC DX
		DEC COLU1
		JNZ H1
		RETN
DIAG:		MOV AH,0CH
		MOV AL,03H
		MOV BH,00H
		MOV CX,C1
		MOV DX,F1
S1:		INT 10H
		INC CX
		DEC DX
		CMP DX,F2
		JNAE S1
		RETN
		END