;AREA DE SUBRUTINAS PARA DIBUJAR FIGURAS HORIZONTAL, VERTICAL Y DIAGONAL

HORIZONTAL:	MOV BP,CCORD2
		SUB BP,CCORD1
		INC BP
		MOV CX,CCORD1
LAZO1:		MOV AH,0CH
		MOV BH,00H
		MOV AL,ATRIBUTO
		MOV DX,FCORD1
		INT 10H
		INC CX
		DEC BP
		JNZ LAZO1
		RET

VERTICAL:       MOV BP,FCORD2	
		SUB BP,FCORD1
		INC BP
		MOV DX,FCORD1
LAZO2:		MOV AH,0CH
		MOV BH,00H
		MOV AL,ATRIBUTO
		MOV CX,CCORD1
		INT 10H
		INC DX
		DEC BP
		JNZ LAZO2
		RET

PENDIENTE_N:	MOV AX,CCORD2
		SUB AX,CCORD1
		MOV BX,FCORD2
		SUB BX,FCORD1
		CMP AX,BX
		JNAE INVIERTE2
		MOV BP,0000H
		MOV DIVISOR,BX
		MOV DX,0000H
		DIV BX
		MOV RESTO,DX
		MOV CUENTA,0000H
		MOV PENDIENTE,AX
		MOV DX,FCORD1
		MOV CX,CCORD1
LAZO30:		MOV DI,PENDIENTE
		ADD DI,BP
		MOV BP,0000H
LAZO31:		MOV AH,0CH
		MOV BH,00H
		MOV AL,ATRIBUTO
		INT 10H
		INC CX
		DEC DI
		JNZ LAZO31
		INC DX
		CMP DX,FCORD2
		JAE SIGU1
		MOV AX,RESTO
		ADD AX,CUENTA
		MOV CUENTA,AX
		CMP AX,DIVISOR
		JNAE SIGUE1
		INC BP
		MOV AX,CUENTA
		SUB AX,DIVISOR
		MOV CUENTA,AX
SIGUE1:		CMP DX,FCORD2
		JNAE LAZO30
SIGU1:		RET
INVIERTE2:   	XCHG AX,BX
		MOV BP,0000H
		MOV DIVISOR,BX
		MOV DX,0000H
		DIV BX
		MOV RESTO,DX
		MOV CUENTA,0000H
		MOV PENDIENTE,AX
		MOV DX,FCORD1
		MOV CX,CCORD1
LAZO32:		MOV DI,PENDIENTE
		ADD DI,BP
		MOV BP,0000H
LAZO33:		MOV AH,0CH
		MOV BH,00H
		MOV AL,ATRIBUTO
		INT 10H
		INC DX
		CMP DX,FCORD2
		JAE SIGU2
		DEC DI
		JNZ LAZO33
		INC CX
		MOV AX,RESTO
		ADD AX,CUENTA
		MOV CUENTA,AX
		CMP AX,DIVISOR
		JNAE SIGUE2
		INC BP
		MOV AX,CUENTA
		SUB AX,DIVISOR
		MOV CUENTA,AX
SIGUE2:		CMP DX,FCORD2
		JNAE LAZO32
SIGU2:		RET

