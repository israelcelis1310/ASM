       .MODEL SMALL                 
       .STACK 100H
	.DATA

	PROM DB 00H
	PAG DB 00H
	FILA DB 00H
	COLUMNA DB 00H
	PR DB "		1.- Horizontal",0AH,0AH,0AH,0DH
	   DB "		2.- Vertical",0AH,0AH,0AH,0DH
	   DB "		3.- Cuadrilatero",0AH,0AH,0AH,0DH
	   DB "	        4.- Diagonal",0AH,0AH,0AH,0DH
	   DB "		5.- Triangulo",0AH,0AH,0AH,0DH
	   DB "		6.- Salir$",0DH
	ET1 DB "	Tipo:",0AH,0AH,0AH,0DH
	    DB "	P1F:",0AH,0AH,0AH,0DH
	    DB "	P2F:",0AH,0AH,0AH,0DH
	    DB "	P1C:",0AH,0AH,0AH,0DH
	    DB "	P2C:",0AH,0AH,0AH,0DH
	FM2 DB "$",0DH
	    DB "	P3F:",0AH,0AH,0AH,0DH
	    DB "	P3C:$"

	CADENA LABEL BYTE
	MAX DB 04H
	NREAL DB ?
	BUFFER DB 04H
	
	TIPO DB 00H
	P1F DW 00H
	P1C DW 00H
	P2F DW 00H
	P2C DW 00H
	P3F DW 00H
	P3C DW 00H
	FORMATO1 DB 0DH DUP(0)
	FORMATO2 DB 13H DUP(0)
	
	
	.CODE
	MOV AX,SEG PROM
	MOV DS,AX
	MOV ES,AX
	
PG0:	CALL BORPA
	CALL PANT
	MOV FILA,04H
	MOV COLUMNA,00H
	CALL CURSOR
	MOV DX,OFFSET PR
	CALL IMPRIME

	
	MOV PAG,00H
	CALL PANT

L1:	CALL TECLA
	CMP AL,"1"
	JE S1
	CMP AL,"2"
	JE S2           
	CMP AL,"3"
	JE S3
	CMP AL,"4"
	JE S4
	CMP AL,"5"
	JE S5
	CMP AL,"6"
	JMP FINAL

S1:	MOV TIPO,"H"
	JMP PG
S2:	MOV TIPO,"V"
	JMP PG
S3:	MOV TIPO,"C"
	JMP PG
S4:	MOV TIPO,"D"
	JMP PG
S5:	MOV TIPO,"T"
	JMP PT

PT:	MOV FM2,00H
	
PG:	MOV PAG,01H
	PUSH AX
	CALL PANT
	MOV FILA,02H
	MOV COLUMNA,00H
	CALL CURSOR
	MOV DX,OFFSET ET1
	CALL IMPRIME
	MOV COLUMNA,13
	CALL CURSOR
	POP AX       
	MOV AH,09H
	MOV AL,TIPO
	MOV BL,07H
	MOV CX,01H
	INT 10H
	
	CMP TIPO,"T"
	JNE SIG
	MOV CX,06
	JMP SIG1
SIG:	MOV CX,04
	INC FILA
	INC FILA
	INC FILA

SIG1:	MOV DI,OFFSET TIPO
	INC DI
VUELVE1:MOV DX,OFFSET CADENA
       
VUELVE:	CALL CURSOR
	MOV DX,OFFSET CADENA
	PUSH DI
	CALL EDITA
 	CALL REVISA
	POP DI
	MOV ES:[DI],BX
	INC DI
	INC DI
	INC FILA 
	INC FILA
	INC FILA
	LOOP VUELVE
	
	JMP FINAL	
	
PG1:	MOV PAG,01H
	CALL PANT
	CALL TECLA
	CMP AL,"1"
	JE SALT2
	CMP AL,"2"
       	JE SALT3
	CMP AL,"3"
	JMP PGA

SALT1: 	JMP PGA; MLT
SALT2:	JMP PGA;SUMA1
SALT3:	JMP PGA;SUMA2                  
SALT4:	JMP PGA;CUAD1
SALT5:	JMP PGA;CUAD2
    
PGA:	MOV PAG,00H
	CALL PANT 
	JMP L1

PG12:	MOV PAG,01H
	CALL PANT                     
	CALL TECLA
	CMP AL,"1"
	JE SALT4
	CMP AL,"2"
       	JE SALT5
	CMP AL,"3"
	JMP PGA


		    ;********INTERRUPCIONES*********
CURSOR:	MOV AH,02H
	MOV BH,PAG
	MOV DH,FILA
	MOV DL,COLUMNA 
	INT 10H 
   	RET

IMPRIME: MOV AH,09H
	INT 21H
	RET

EDITA:	MOV AH,0AH
	INT 21H
	RET

PANT:	MOV AH,05H
	MOV AL,PAG
	INT 10H
	RET

TECLA:	MOV AH,00H
	INT 16H
	RET

BORPA:	MOV AH,00H
	MOV AL,03H
	INT 10H
	RET
	

REVISA:	MOV SI,OFFSET BUFFER
R0:	MOV AL,DS:[SI]
	
	CMP AL,30H
	JAE R1
	JMP VUELVE
R1:	CMP AL,39H
	JBE R2
	JMP VUELVE
R2:	AND AL,0FH
	JMP R5
	CMP AL,41H
	JAE R3
	JMP VUELVE
R3:	CMP AL,46H
	JBE R4
	JMP VUELVE
R4:	SUB AL,37H
R5:	INC SI
	CALL GUARDA
	DEC NREAL
	JNZ R0
RF:	RET

GUARDA:	CMP NREAL,03H
	JE GUA1
	CMP NREAL,02H
	JE GUA2
	ADD BL,AL
	JMP FG
GUA1:	MOV BH,AL
	JMP FG
GUA2:	MOV BL,AL
	ROL BL,04H
FG:	RET

ERROR:	MOV AX,4C01H
	JMP FINAL1

FINAL:	MOV AX,4C00H

FINAL1:	INT 21H
	END
	
						     
