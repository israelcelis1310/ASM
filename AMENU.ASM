       .MODEL SMALL                 
       .STACK 100H
	.DATA

	TAB1 DB 02H,04H,05H,09H,00H,02H,04H,06H,00H,02H,04H,05H,04H,09H,09H,08H,08H,06H,05H,04H,01H,03H,05H,02H,0AH,06H,0BH,01H,09H,01H,00H,04H
	TAB2 DB 20H,02H,09H,0AH,00H,08H,03H,06H,09H,06H,02H,04H,02H,05H,01H,04H,00H,05H,09H,01H,04H,06H,00H,02H,04H,05H,04H,09H,09H,08H,08H,06H
	TABM DW 20H DUP(?)            
	TABC DW 20H DUP(?)           
	PROM DB 00H
	PAG DB 00H

	FILA DB 00H
	COLUMNA DB 00H
	PR DB "1.- Promedio$"
	MU DB "2.- Multiplica$"
	CU DB "3.- Cuadrado$"
	SA DB "4.- Salir$"
	ET1 DB "1.- Tabla 1$", 0AH, 0DH
	ET2 DB "2.- Tabla 2$",0AH, 0DH
	RETOR DB "3.- Retornar$"

	.CODE
	MOV AX,SEG TAB1
	MOV DS,AX
	MOV ES,AX
	MOV SI,OFFSET TAB1         
	MOV DI,OFFSET TAB2            


PG0:	CALL BORPA
	CALL PANT
	MOV FILA,04H
	MOV COLUMNA,06H
	CALL CURSOR
	MOV DX,OFFSET PR
	CALL IMPRIME
	INC FILA
	INC FILA
	CALL CURSOR
	MOV DX,OFFSET MU
	CALL IMPRIME
	INC FILA
	INC FILA
	CALL CURSOR
	MOV DX,OFFSET CU
	CALL IMPRIME
	INC FILA
	INC FILA
	CALL CURSOR
	MOV DX,OFFSET SA
	CALL IMPRIME
	CALL TECLA
	CMP AL,"1"
	JE PG1 
	CMP AL,"2"
	JE SALT1           
	CMP AL,"3"
	JE PG12
	CMP AL,"4"
	JMP FINAL


PG1:	MOV PAG,01H
	CALL BORPA
	CALL PANT
	MOV FILA,04H
	MOV COLUMNA,06H
	CALL CURSOR2
	MOV DX,OFFSET ET1
	CALL IMPRIME
	INC FILA
	INC FILA
	CALL CURSOR2
	MOV DX,OFFSET ET2
	CALL IMPRIME
	ADD FILA,02H	
	CALL CURSOR2
	MOV DX,OFFSET RETOR
	CALL IMPRIME
	INC FILA
	INC FILA
	CALL TECLA
	CMP AL,"1"
	JE SALT2
	CMP AL,"2"
       	JE SALT3
	CMP AL,"3"
	JMP PG0

SALT1: 	JMP MLT
SALT2:	JMP SUMA1
SALT3:	JMP SUMA2
SALT4:	JMP CUAD1
SALT5:	JMP CUAD2
    
PG12:	MOV PAG,01H
	CALL BORPA
	CALL PANT
	MOV FILA,04H
	MOV COLUMNA,06H
	CALL CURSOR2
	MOV DX,OFFSET ET1
	CALL IMPRIME
	INC FILA
	INC FILA
	CALL CURSOR2
	MOV DX,OFFSET ET2
	CALL IMPRIME
	INC FILA
	INC FILA
	CALL CURSOR2
	MOV DX,OFFSET RETOR
	CALL IMPRIME
	CALL TECLA
	CMP AL,"1"
	JE SALT4
	CMP AL,"2"
       	JE SALT5
	CMP AL,"3"
	JMP PG0


		    ;********INTERRUPCIONES*********
CURSOR:	MOV AH,02H
	MOV BH,00H
	MOV DH,FILA
	MOV DL,COLUMNA 
	INT 10H 
   	RET

CURSOR2:	MOV AH,02H
	MOV BH,01H
	MOV DH,FILA
	MOV DL,COLUMNA 
	INT 10H 
	RET


IMPRIME: MOV AH,09H
	INT 21H
	RET

PANT:	MOV AH,05H
	MOV AL,PAG
	INT 10H
	RET

TECLA:	MOV AH,00H
	INT 16H
	RET

BORPA:	MOV AH,00H
	MOV AL,03H
	INT 10H
	RET

	       	
SUMA1:  MOV SI,OFFSET TAB1
	MOV DI,OFFSET TAB2

	MOV AX,0000H
	MOV CX,0020H
	MOV DX,0000H

S1:	MOV AL,DS:[SI]
	ADD DX,AX
	INC SI
	LOOP S1
	JMP FIN

SUMA2: 	MOV DI,OFFSET TAB2

	MOV AX,0000H
	MOV CX,0020H
	MOV DX,0000H

S2:	MOV AL,DS:[DI]
	ADD DX,AX
	INC DI
	LOOP S2
         
FIN:	MOV AX,DX
	MOV BL,20H
	DIV BL
	MOV PROM,AL
	JMP FINAL

CUAD1:	MOV SI,OFFSET TAB1
	MOV DI,OFFSET TABC

	MOV AX,0000H
	MOV BX,0000H
	MOV CX,0020H

CD1:	MOV AL,DS:[SI] 	
	MOV BL,AL
	MUL BL
	MOV DS:[DI],AX
	INC SI
	INC DI
	INC DI
	LOOP CD1

	JMP FINAL

CUAD2:	MOV SI,OFFSET TAB2	
	MOV DI,OFFSET TABC
	
	MOV AX,0000H
	MOV BX,0000H
	MOV CX,0020H

CD2:	MOV AL,DS:[SI] 	
	MOV BL,AL                     
	MUL BL
	MOV DS:[DI],AX
	INC SI
	INC DI
	INC DI                      
	LOOP CD2

	JMP FINAL
	
MLT:	MOV SI,OFFSET TAB1
	MOV DI,OFFSET TABM
	MOV AX,0000H
	MOV BX,0000H
	MOV CX,0020H

M1:	MOV AL,DS:[SI]
	MOV BL,DS:[SI+20H]
	MUL BL
	MOV DS:[DI],AX
	INC SI
	INC DI
	INC DI
	LOOP M1
 
	JMP FINAL
	  
ERROR:	MOV AX,4C01H
	JMP FINAL1

FINAL:	MOV AX,4C00H

FINAL1:	INT 21H
	END
	
						     
